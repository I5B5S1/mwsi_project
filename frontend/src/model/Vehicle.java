package model;

public class Vehicle {
	private String vin;
	private String brand;
	private String model;
	private String generation;
	private String bodyType;
	private int amountOfSeats;
	private int engineCapacity;
	private int power;
	private String vehicleCardSeries;
	private String vehicleCardNumber;
	private int maxTotalWeight;
	private int ownWeight;
	private int axesAmount;
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getGeneration() {
		return generation;
	}
	public void setGeneration(String generation) {
		this.generation = generation;
	}
	public String getBodyType() {
		return bodyType;
	}
	public void setBodyType(String bodyType) {
		this.bodyType = bodyType;
	}
	public int getAnountOfSeats() {
		return amountOfSeats;
	}
	public void setAnountOfSeats(int anountOfSeats) {
		this.amountOfSeats = anountOfSeats;
	}
	public int getEngineCapacity() {
		return engineCapacity;
	}
	public void setEngineCapacity(int engineCapacity) {
		this.engineCapacity = engineCapacity;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public String getVehicleCardSeries() {
		return vehicleCardSeries;
	}
	public void setVehicleCardSeries(String vehicleCardSeries) {
		this.vehicleCardSeries = vehicleCardSeries;
	}
	public String getVehicleCardNumber() {
		return vehicleCardNumber;
	}
	public void setVehicleCardNumber(String vehicleCardNumber) {
		this.vehicleCardNumber = vehicleCardNumber;
	}
	public int getMaxTotalWeight() {
		return maxTotalWeight;
	}
	public void setMaxTotalWeight(int maxTotalWeight) {
		this.maxTotalWeight = maxTotalWeight;
	}
	public int getOwnWeight() {
		return ownWeight;
	}
	public void setOwnWeight(int ownWeight) {
		this.ownWeight = ownWeight;
	}
	public int getAxesAmount() {
		return axesAmount;
	}
	public void setAxesAmount(int axesAmount) {
		this.axesAmount = axesAmount;
	}
	
	
}
