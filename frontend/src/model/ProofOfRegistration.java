package model;

public class ProofOfRegistration {
	private String registrationNumber;
	private String series;
	private String number;
	private String issuingAuthority;
	private String registrationDate;
	private String registrationCountry;
	private String firstRegistrationDate;
	private String expireDate;
	private String examinationDate;
	private String nextExaminationDate;
	public String getRegistrationNumber() {
		return registrationNumber;
	}
	public void setRegistrationNumber(String registrationNumber) {
		this.registrationNumber = registrationNumber;
	}
	public String getSeries() {
		return series;
	}
	public void setSeries(String series) {
		this.series = series;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getIssuingAuthority() {
		return issuingAuthority;
	}
	public void setIssuingAuthority(String issuingAuthority) {
		this.issuingAuthority = issuingAuthority;
	}
	public String getRegistrationDate() {
		return registrationDate;
	}
	public void setRegistrationDate(String registrationDate) {
		this.registrationDate = registrationDate;
	}
	public String getRegistrationCountry() {
		return registrationCountry;
	}
	public void setRegistrationCountry(String registrationCountry) {
		this.registrationCountry = registrationCountry;
	}
	public String getFirstRegistrationDate() {
		return firstRegistrationDate;
	}
	public void setFirstRegistrationDate(String firstRegistrationDate) {
		this.firstRegistrationDate = firstRegistrationDate;
	}
	public String getExpireDate() {
		return expireDate;
	}
	public void setExpireDate(String expireDate) {
		this.expireDate = expireDate;
	}
	public String getExaminationDate() {
		return examinationDate;
	}
	public void setExaminationDate(String examinationDate) {
		this.examinationDate = examinationDate;
	}
	public String getNextExaminationDate() {
		return nextExaminationDate;
	}
	public void setNextExaminationDate(String nextExaminationDate) {
		this.nextExaminationDate = nextExaminationDate;
	}
	
	
}
